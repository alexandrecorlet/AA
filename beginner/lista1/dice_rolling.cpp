#include <bits/stdc++.h>
using namespace std;

int main() {
    int t;
    scanf("%d", &t);
    while (t--) {
        int p;
        scanf("%d", &p);
        printf("%d\n", p / 2);
    }
    return 0;
}

// Time Complexity: O(t) | Space Complexity O(1), where
// t is the number of test cases.

